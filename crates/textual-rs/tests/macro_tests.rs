/// Integration tests for the textual-rs-macros proc-macro crate.
///
/// Tests verify that #[derive(Widget)] and #[widget_impl] work correctly:
/// - widget_type_name returns struct name
/// - #[focusable] makes can_focus() return true
/// - on_mount wires own_id, on_unmount clears it
/// - #[on(T)] dispatches events via on_event
/// - #[keybinding("key", "action")] wires key_bindings and on_action
use std::any::Any;
use std::cell::Cell;
use ratatui::{buffer::Buffer, layout::Rect};
use textual_rs::widget::context::AppContext;
use textual_rs::widget::{Widget, WidgetId, EventPropagation};

// ---------------------------------------------------------------------------
// Test 1: Basic derive + widget_impl generates widget_type_name and on_mount
// ---------------------------------------------------------------------------

#[derive(textual_rs::Widget)]
struct BasicWidget {
    own_id: Cell<Option<WidgetId>>,
}

impl BasicWidget {
    fn new() -> Self {
        Self {
            own_id: Cell::new(None),
        }
    }
}

/// #[widget_impl] injects widget_type_name, can_focus, on_mount, on_unmount
/// by delegating to the __ helpers generated by #[derive(Widget)].
#[textual_rs::widget_impl]
impl Widget for BasicWidget {
    fn render(&self, _ctx: &AppContext, _area: Rect, _buf: &mut Buffer) {}
}

#[test]
fn derive_widget_basic() {
    let w = BasicWidget::new();
    assert_eq!(w.widget_type_name(), "BasicWidget");
    assert!(!w.can_focus());
}

#[test]
fn derive_widget_on_mount_sets_id() {
    let w = BasicWidget::new();
    assert!(w.own_id.get().is_none());

    // Create a minimal context and insert a widget to get a valid WidgetId
    let mut ctx = AppContext::new();
    let id = ctx.arena.insert(Box::new(BasicWidget::new()) as Box<dyn Widget>);

    w.on_mount(id);
    assert_eq!(w.own_id.get(), Some(id));

    w.on_unmount(id);
    assert!(w.own_id.get().is_none());
}

